import {
  Button,
  Card,
  CardContent,
  Container,
  Divider,
  Grid,
} from "@material-ui/core";
import { Alert } from "@material-ui/lab";
import React, { useEffect } from "react";
import { useDispatch, useSelector } from "react-redux";
import { createOrder } from "../../actions/orderActions";
import Meta from "../../components/Meta";
import StepperNav from "../../components/StepperNav";
import { OrderItem, ShippingMessage, SummaryItem } from "./PlaceOrder.elements";

const PlaceOrder = ({ history }) => {
  const cart = useSelector((state) => state.cart);

  cart.itemsPrice = cart.cartItems
    .reduce((acc, item) => acc + item.price * item.qty, 0)
    .toFixed(2);

  cart.shippingPrice = cart.itemsPrice > 50 ? 0.0 : 10.0;

  cart.totalPrice = Number(cart.itemsPrice + cart.shippingPrice).toFixed(2);

  const orderCreate = useSelector((state) => state.orderCreate);

  const { order, success, error } = orderCreate;

  useEffect(() => {
    if (success) {
      history.push(`/order/${order._id}`);
    }
    // eslint-disable-next-line
  }, [history, success]);

  const dispatch = useDispatch();
  const handleSubmit = () => {
    dispatch(
      createOrder({
        orderItems: cart.cartItems,
        shippingAddress: cart.shippingAddress,
        paymentMethod: cart.paymentMethod,
        itemsPrice: cart.itemsPrice,
        shippingPrice: cart.shippingPrice,
        totalPrice: cart.totalPrice,
      })
    );
  };

  return (
    <div className="placerorder-page">
      <Meta title="Your Order | Book Attic" />
      <Container maxWidth={"lg"}>
        <StepperNav stepNumber={3} />
        <Grid container spacing={4}>
          <Grid item xs={12} md={8}>
            <h3>SHIPPING</h3>
            <p>
              <strong>Address: </strong>
              {`${cart.shippingAddress.address}, ${cart.shippingAddress.city}, ${cart.shippingAddress.postalCode} ${cart.shippingAddress.country}`}
            </p>
            <Divider />
            <h3>PAYMENT</h3>
            <p>
              <strong>Method: {cart.paymentMethod} </strong>
            </p>
            <Divider />
            <h3>Order Items</h3>
            {cart.cartItems.length === 0 ? (
              <Alert>Your Cart is Empty</Alert>
            ) : (
              cart.cartItems.map((item) => (
                <div key={item._id}>
                  <OrderItem>
                    <img
                      style={{ width: "50px" }}
                      src={item.image}
                      alt={item.name}
                    />
                    <p>{item.name}</p>
                    <p>{`${item.qty} x ${item.price} = ${
                      item.qty * item.price
                    }`}</p>
                  </OrderItem>
                  <Divider />
                </div>
              ))
            )}
          </Grid>
          <Grid item xs={12} md={4}>
            <Container>
              <Card>
                <CardContent>
                  <h3>ORDER SUMMARY</h3>
                  <Divider />
                  <SummaryItem>
                    Items: <strong>TK{cart.itemsPrice}</strong>
                  </SummaryItem>
                  <SummaryItem>
                    Shipping: <strong>TK{cart.shippingPrice}</strong>
                  </SummaryItem>
                  <SummaryItem>
                    Total: <strong>TK{cart.totalPrice}</strong>
                  </SummaryItem>
                  <ShippingMessage>
                    (Orders above TK50 have free shipping)
                  </ShippingMessage>
                  {error ? <Alert severity="error">{error}</Alert> : <></>}
                  <Divider />
                  <Button
                    style={{ marginTop: "12px" }}
                    variant="contained"
                    color="primary"
                    onClick={handleSubmit}
                  >
                    PLACE ORDER
                  </Button>
                </CardContent>
              </Card>
            </Container>
          </Grid>
        </Grid>
      </Container>
    </div>
  );
};

export default PlaceOrder;
